#More intereseting tests

#Testing for equality and inequality between string
print("Is 'ajay' equal to 'Ajay'? I predict false")
print('ajay' == 'Ajay')

print("Is 'ajay' not equal to 'Ajay'? I predict true")
print('ajay' != 'Ajay')

print("Is 'ajay' equal to 'Ajay' lower? I predict true")
print('ajay' == 'Ajay'.lower())

print("Is 'ajaY' equal to 'Ajay' lower? I predict false")
print('ajay' != 'Ajay'.lower())

print("Is 'ajAY' lower equal to 'Ajay' lower? I predict true")
print('ajAY'.lower() == 'Ajay'.lower())

#Boolean tests using 'and', 'or'
print('1 and 0 is 0')
print(1 and 0)

print('1 or 0 is 1')
print(1 or 0)



